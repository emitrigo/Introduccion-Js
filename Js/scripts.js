// Etiquetas let, var y const

let nombre = "Emiliano";
console.log(nombre);

var edad = 29;
console.log(edad);

const mascota = "Lola";
console.log(mascota);

//LET: Podemos cambiarle el dato pero podemos NO REDECLARARLO
//let nombre = "Chiara";
nombre= "Chiara"
console.log(nombre)

//VAR: Podemos redeclarar y cambiar un dato nuevo por el anterior
var edad = 22
console.log(edad);

// CONST: No nos permite REDECLARAR ni REASIGNAR un valor a una variable, es decir que no se puede cambiar el nombre de Lola en este caso
mascota = "Candela";
console.log(mascota);






